CREATE TABLE LOB_IMPUESTOS(
	RefNbr varchar(10) NOT NULL,
	BatNbr varchar(10) NOT NULL,
	PerPost char(6) NOT NULL,
	apdoc_id int NULL,
	ardoc_id int NULL,
	DocType char(2) NULL,
	TranTot numeric NULL,
	TaxAmt00 numeric NULL,
	TaxAmt01 numeric NULL,
	TaxTot numeric NULL,
)

CREATE NONCLUSTERED INDEX IDX_APDOC_ID_LOB_IMPUESTOS
ON LOB_IMPUESTOS(apdoc_id ASC)

CREATE NONCLUSTERED INDEX IDX_ARDOC_ID_LOB_IMPUESTOS
ON LOB_IMPUESTOS(ardoc_id ASC)

CREATE NONCLUSTERED INDEX IDX_REFNBR_LOB_IMPUESTOS
ON LOB_IMPUESTOS(refnbr ASC)

CREATE NONCLUSTERED INDEX IDX_BATNBR_LOB_IMPUESTOS
ON LOB_IMPUESTOS(batnbr ASC)

CREATE NONCLUSTERED INDEX IDX_TRANTOT_LOB_IMPUESTOS
ON LOB_IMPUESTOS(TranTot ASC)